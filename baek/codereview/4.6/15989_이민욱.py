import sys
sys.stdin = open('input.txt','r')
input = sys.stdin.readline

dp = [1] * 10001 # 1만 사용
# 1번 print(dp[:10])

for i in range(2, 10001): # 1,2 사용
    dp[i] += dp[i-2]
# 2번 print(dp[:10])

for i in range(3, 10001): # 1,2,3 사용
    dp[i] += dp[i-3]
# 3번 print(dp[:10])

for _ in range(int(input())):
    print(dp[int(input())])



